import Foundation
import UIKit

extension String {

    enum StaticTexts {

        static var flipsLabelText: String {
            return "Flips: "
        }

        static var blankLabelText: String {
            return ""
        }

        static var scoreLabelText: String {
            return "Score: "
        }

        static var endGameTitle: String {
            return "The End!"
        }

        static var startNewGameText: String {
            return "Start New Game"
        }

    }

}
